function drawGrid(level) {
    return elt("table", {
        class: "background",
        style: `width: ${level.width * scale}px`
    }, ...level.rows.map(row =>
        elt("tr", { style: `height: ${scale}px` },
            ...row.map(type => elt("td", { class: type })))))
}

function drawActors(actors) {
    return elt('div', {},...actors.map( actor => {
        let rect = elt('div', {class: `actor ${actor.type}`});
        rect.style.width = `${actor.size.x * scale}px`;
        rect.style.height = `${actor.size.y * scale}px`;
        rect.style.left = `${actor.pos.x * scale}px`;
        rect.style.top = `${actor.pos.y * scale}px`
        return rect;
    }))
}
 
DOMDisplay.prototype.syncState = function(state) {
    if (this.actorLayer) this.actorLayer.remove();
    this.actorLayer = drawActors(state.actors);
    this.dom.appendChild(this.actorLayer);
    this.dom.className = `game ${state.status}`;
    this.scrollPlayerIntoView(state);
};

DOMDisplay.prototype.scrollPlayerIntoView = function (state) {
    let width = this.dom.clientWidth;
    let height = this.dom.clientHeight;
    let margin = width / 3;

    //The viewport

    let left = this.dom.scrollLeft, right = left + width;
    let top = this.dom.scrollTop, bottom = top + height;

    let player = state.player;
    let center = player.pos.plus(player.size.times(0.5)).times(scale);

    if (center.x < left + margin) {
        this.dom.scrollLeft = center.x - margin;
    } else if (center.x > right - margin) {
        this.dom.scrollLeft = center.x + margin - width;
    }
    if (center.y < top + margin) {
        this.dom.scrollTop = center.y - margin;
    } else if (center.y > bottom - margin) {
        this.dom.scrollTop = center.y + margin - height;
    }
}



State.prototype.update = function(time, keys) {
    let updatedActors = this.actors.map( (actor) => {
        return actor.update(time, this, keys);
    }
    );
    
    let newState = new State(this.level, updatedActors, this.status);

    if (newState.status != 'playing') return newState;

    let player = newState.player;

    if (this.level.touches(player.pos, player.size, 'lava')) {
        return new State(this.level, updatedActors, 'lost');
    }

    for (let actor of updatedActors) {
        if (actor != player && overlap(actor, player)) {
            newState = actor.collide(newState);
        }
    }

    return newState;
}

Lava.prototype.update = function(time, state) {

    let pos = this.pos;
    let newPos = pos.plus(this.speed.times(time));
    if (!state.level.touches(newPos, this.size, 'wall')) {
        return new Lava(newPos, this.speed, this.reset);
    } else if (this.reset) {
        return new Lava(this.reset, this.speed, this.reset);
    } else {
        return new Lava(this.pos, this.speed.times(-1));
    }
    
}

